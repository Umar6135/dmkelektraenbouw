                        </p>
                        <h5>1 fase groepenkast
                        </h5>
                        <p class="theme-paragraph">De eerste soort groepenkast die we behandelen, en tevens de meest gangbare in Nederlandse woningen, is de 1 fase groepenkast. Deze is typisch uitgerust met een 2-polige aardlekschakelaar en een 2-polige hoofdschakelaar. Voor het merendeel van de huishoudens biedt deze groepenkast een adequate oplossing voor de elektriciteitsvoorziening, hoewel dit ook afhangt van de specifieke elektrische apparaten die in gebruik zijn. Een kenmerkend aspect van de 1 fase groepenkast is dat de modules een breedte hebben van 36 millimeter.


                        </p>
<h5>3 fase groepenkast
</h5>
                        <p class="theme-paragraph">Een andere veel voorkomende variant is de 3 fase groepenkast. Net als de 1 fase groepenkast, beschikt ook deze over een 2-polige aardlekschakelaar. Het verschil zit echter in de hoofdschakelaar, die bij een 3 fase groepenkast 4-polig is. Dit resulteert in een dubbele breedte van de module in de groepenkast, namelijk 72 millimeter. Deze groepenkast staat ook bekend als een 3P+N automaat, wat staat voor ‘3-polig + nul’. Hoewel u deze variant ook in woonhuizen kunt aantreffen, is het vaker gebruikelijk in bedrijfspanden. Een belangrijk voordeel van de 3 fase groepenkast is dat het mogelijkheden biedt voor een krachtstroomaansluiting.



